'''

Write the necessary code to display the area and perimeter of a rectangle that has a width of 2.4 and a height of 6.4.

'''
rect_length = 6.4
rect_width = 2.4
rect_area = rect_length * rect_width
rect_perimeter = 2 * (rect_length + rect_width)
print(f"Area of the rectangle: {rect_area}")
print(f"Perimeter of the rectangle: {rect_perimeter}")

